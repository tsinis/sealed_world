/// An [Enum] representing the base element shape on a flag.
enum FlagElementsType {
  /// A flag with an ellipse shape element.
  ellipse,

  /// A flag with a moon shape element.
  moon,

  /// A flag with multiple simple different elements.
  multiElement,

  /// A flag with a rectangle shape element.
  rectangle,

  /// A flag with a star shape element.
  star,

  /// A flag with a triangle shape element.
  triangle,
}
